import fs from 'node:fs/promises';
import path from 'node:path';
import { watch } from 'node:fs';
import { fileURLToPath } from 'node:url';

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);
const repoRoot = path.resolve(__dirname, '../../..');
const personasDir = path.join(repoRoot, 'personas');
const outputFile = path.join(repoRoot, 'packages/workload-router/src/generated/personas.ts');

const exportNameMap = new Map([
  ['frontend-implementer', 'frontendImplementer'],
  ['code-reviewer', 'codeReviewer'],
  ['architecture-planner', 'architecturePlanner'],
  ['requirements-analyst', 'requirementsAnalyst'],
  ['debugger', 'debuggerPersona'],
  ['security-reviewer', 'securityReviewer'],
  ['technical-writer', 'technicalWriter'],
  ['verifier', 'verifierPersona'],
  ['test-strategist', 'testStrategist'],
  ['tdd-guard', 'tddGuard'],
  ['flake-hunter', 'flakeHunter'],
  ['opencode-workflow-specialist', 'opencodeWorkflowSpecialist'],
  ['npm-provenance-publisher', 'npmProvenancePublisher'],
  ['cloud-sandbox-infra', 'cloudSandboxInfra'],
  ['sage-slack-egress-migrator', 'sageSlackEgressMigrator'],
  ['sage-proactive-rewirer', 'sageProactiveRewirer'],
  ['cloud-slack-proxy-guard', 'cloudSlackProxyGuard'],
  ['agent-relay-e2e-conductor', 'agentRelayE2eConductor'],
  ['capability-discoverer', 'capabilityDiscoverer'],
  ['posthog', 'posthogAgent']
]);

async function generate() {
  const files = (await fs.readdir(personasDir)).filter((n) => n.endsWith('.json')).sort();
  const lines = [
    '// AUTO-GENERATED by packages/workload-router/scripts/generate-personas.mjs',
    '// Do not edit by hand. Source of truth: /personas/*.json',
    ''
  ];
  for (const file of files) {
    const basename = file.replace(/\.json$/, '');
    const exportName = exportNameMap.get(basename);
    if (!exportName) {
      console.warn(`[generate-personas] skipping unwired persona: ${basename} (no export name mapping)`);
      continue;
    }
    const json = await fs.readFile(path.join(personasDir, file), 'utf8');
    lines.push(`export const ${exportName} = ${json.trim()} as const;`);
    lines.push('');
  }
  await fs.writeFile(outputFile, lines.join('\n'));
}

await generate();

if (process.argv.includes('--watch')) {
  // Debounce to coalesce editor save bursts (write-temp + rename fires 2-3
  // events for a single logical save on most editors). 100ms is enough to
  // coalesce without adding noticeable latency.
  let pending;
  const DEBOUNCE_MS = 100;
  const trigger = (filename) => {
    if (filename && !filename.endsWith('.json')) return;
    clearTimeout(pending);
    pending = setTimeout(async () => {
      const label = filename ? `personas/${filename}` : 'personas/';
      try {
        await generate();
        console.log(`[generate-personas] regenerated (${label} changed)`);
      } catch (err) {
        console.error(`[generate-personas] regeneration failed: ${err.message}`);
      }
    }, DEBOUNCE_MS);
  };
  watch(personasDir, { persistent: true }, (_event, filename) => trigger(filename));
  console.log(`[generate-personas] watching ${personasDir}`);
}
